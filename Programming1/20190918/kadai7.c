#include <stdio.h>

int main(void)
{
    printf("円周率は %f ... です. \n", 3.141592653589793);
    printf("%%f で小数を表示するとき, 小数第何位までを表示するのかを指定できます. \n");
    printf("円周率を小数第2位まで表示すると %4.2f です. \n", 3.141592653589793);
    printf("円周率を小数第5位まで表示すると %7.5f です. \n", 3.141592653589793);
    printf("円周率を小数第13位まで表示すると %15.13f です. \n\n", 3.141592653589793);

    printf("円周率を小数第26位まで表示すると %28.26f です. \n", 3.141592653589793);
    printf("このように下位の桁を無理やり表示すると, でたらめな数が出力されます. \n");
    return 0;
}